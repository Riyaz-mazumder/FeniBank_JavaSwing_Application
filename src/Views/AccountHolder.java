    public void data(String ok){
       List<SavingsAccountModel> all = AccountHolderDB.gelAllSavingAcc(ok); 
       String[] clos = {"Owner Name", "Account Number", "Withdraw Limit", "Transaction Limit Per Day", "Blance", "Interest" };
       String[][] rows = new String[all.size()][clos.length];  
         for(int i=0;i<all.size();i++){
             rows[i][0] = all.get(i).getOwner();
             rows[i][1] = all.get(i).getAccountNumber();
             rows[i][2] = all.get(i).getWithdrawLimit();
             rows[i][3] = all.get(i).getTransactionLimit();
             rows[i][4] = String.valueOf(all.get(i).getDipositMoney());
             rows[i][5] = all.get(i).getInterest();
         }
     DefaultTableModel dtm = new DefaultTableModel(rows, clos);
     jTable2.setModel(dtm);
    }
